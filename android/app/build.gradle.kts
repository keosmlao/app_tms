// Load release signing config from android/key.properties (gitignored).
// File is required for assembleRelease / bundleRelease so an accidental
// "missing key.properties" doesn't silently fall back to debug signing —
// that would produce an AAB Play Console rejects with INSTALL_PARSE_FAILED.
val keystoreProperties = Properties().apply {
    val keystorePropertiesFile = rootProject.file("key.properties")
    if (keystorePropertiesFile.exists()) {
        load(FileInputStream(keystorePropertiesFile))
    }
}

